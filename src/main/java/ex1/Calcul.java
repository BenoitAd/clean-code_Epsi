package ex1;

/**
 * Cette classe contient des méthodes pour effectuer des calculs mathématiques simples.
 */
public class Calcul {


    /**
     * Cette méthode calcule et retourne l'addition de deux nombres entiers.
     *
     * @param a Le premier nombre entier à additionner.
     * @param b Le deuxième nombre entier à additionner.
     * @return La somme des deux nombres entiers (a + b).
     */
    public int getAddition(int a, int b) {
        return a + b;
    }

    /**
     * Cette méthode calcule et retourne la soustraction de deux nombres entiers.
     *
     * @param a Le premier nombre entier.
     * @param b Le deuxième nombre entier.
     * @return La différence entre les deux nombres entiers (a - b).
     */
    public int getSoustration(int a, int b) {
        return a - b;
    }
}
